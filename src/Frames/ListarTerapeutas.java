    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        if (evt.getClickCount() == 2 && tabla.getSelectedRow() >= 0) {
            String estado = tabla.getValueAt(tabla.getSelectedRow(), 3).toString();
            if (!estado.equalsIgnoreCase("Activo")) {
                new rojerusan.RSNotifyFade("Usuario Inactivo", "Debe seleccionar un terapeuta que se encuentre activo", 5,
                        RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.INFORMATION).setVisible(true);

            } else {
                int idTerapeuta = Integer.parseInt(tabla.getValueAt(tabla.getSelectedRow(), 0).toString());

                Usuario seleccionado = users.stream()
                        .filter(t-> Float.compare(t.getId(), idTerapeuta) == 0)
                        .findFirst()
                        .orElse(new Usuario());
                
                if (uso.equalsIgnoreCase("newPaciente")) {
                    RnpFinalizar.setTerapeuta(seleccionado);

                }else
                if (uso.equalsIgnoreCase("HistorialPaciente")) {
                    
                }else
                if (uso.equalsIgnoreCase("asignarTerapeuta")) {
                    
                     new AsignarCita(index,seleccionado,paciente).setVisible(true);
                            
                }
            this.dispose();   
            }
        }
    }//GEN-LAST:event_tablaMouseClicked

