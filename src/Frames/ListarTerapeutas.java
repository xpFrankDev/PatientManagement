    private static void actualizarValores() {
        miGrupoRadio.setVisible(false);
        todosRadio.setVisible(false);
        
        users.clear();
        barra.setVisible(true);
        String texto = nombreTxt.getText().isEmpty() ? "" : nombreTxt.getText();
        users = miGrupoRadio.isSelected()
                ? (ArrayList) new Usuario().getUsuarioxJefe(3, texto).clone()
                : (ArrayList) new Usuario().getUsuarioxCodigo(texto).clone();

        llenarTabla();
    }

    private static void llenarTabla() {
        String texto = nombreTxt.getText().isEmpty() ? "" : nombreTxt.getText();
        modelo.setRowCount(0);

        for (Usuario user : users) {
            if (user.getCargo().equalsIgnoreCase("terapeuta")) {
                if (user.getNombre().contains(texto) || user.getCodigo().contains(texto)) {
                    modelo.addRow(new Object[]{
                        user.getId(), user.getCodigo(), user.getNombre(), user.getEstado()
                    });
                }
            }

        }
        barra.setVisible(false);
    }

