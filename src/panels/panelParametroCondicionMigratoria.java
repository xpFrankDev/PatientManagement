    public void iniciar() {
        llenarGeneros("");
    }

    private void reset() {
        resetBtn.setEnabled(false);
        limpiarCampos();
        resetBtn.setEnabled(true);

    }

    public void llenarGeneros() {
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        modelo.setRowCount(0);
        condiciones.clear();
        condiciones = (ArrayList) new CondicionMigratoria().Listar("").clone();
        for (CondicionMigratoria condicion : condiciones) {
            modelo.addRow(new Object[]{
                condicion.getId(), condicion.getNombre(), condicion.getEstado()
            });
        }
        if (condiciones.size() < 1) {
            modelo.addRow(new Object[]{
                "-", "Sin Resultados"
            });
        }
    }
    public void llenarGeneros(String estadox) {
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        modelo.setRowCount(0);
        condiciones.clear();
        condiciones = (ArrayList) new CondicionMigratoria().Listar(estadox).clone();
        for (CondicionMigratoria condicion : condiciones) {
            if (condicion.getNombre().contains(estadox)) {
                modelo.addRow(new Object[]{
                condicion.getId(), condicion.getNombre(), condicion.getEstado()
            });
            }
            
        }
        if (condiciones.size() < 1) {
            modelo.addRow(new Object[]{
                "-", "Sin Resultados"
            });
        }
    }

    private void activarDesactivar(boolean llave) {
        nombreTxt.setEditable(llave);
        activoRb.setEnabled(llave);
        bloqueadoRb.setEnabled(llave);
        activoRb.setSelected(true);
    }

    private void limpiarCampos() {
        valor = -1;
        activarDesactivar(false);
        nombreTxt.setText("");
        idTxt.setText("-");
        activoRb.setEnabled(false);
        bloqueadoRb.setEnabled(false);
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        modelo.setRowCount(0);
        nuevoBtn.setEnabled(agregar);
        editarBtn.setEnabled(editar);
        guardarBtn.setEnabled(false);
        resetBtn.setEnabled(true);
        llenarGeneros("");
    }

    private boolean validarCamposLlenos() {
        boolean llave = false;
        if (nombreTxt.getText().length() > 0) {
            llave = true;
        }
        return llave;
    }

    public void buscarValorTabla(String nombrex) {
        for (int i = 0; i < tabla.getRowCount(); i++) {
            if (tabla.getValueAt(i, 1).equals(nombrex)) {
                tabla.changeSelection(i, 1, false, false);
            }
        }
    }

