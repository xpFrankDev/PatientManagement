    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBtnActionPerformed
        
        int fila = tabla.getSelectedRow();
        String id = fila >= 0 ? tabla.getValueAt(fila, 0).toString() : "-";
        if (fila >= 0 && !id.equals("-")) {
            activarDesactivar(true);
            valor = 1;
            editarBtn.setEnabled(false);
            int idint = Integer.valueOf(id);
            valor = 1;
            for (CondicionMigratoria condicion : condiciones) {
                if (idint == condicion.getId()) {
                    idTxt.setText(condicion.getId() + "");
                    nombreTxt.setText(condicion.getNombre());
                    if (condicion.getEstado().equalsIgnoreCase("activo")) {
                        activoRb.setSelected(true);
                    } else {
                        bloqueadoRb.setSelected(true);
                    }
                }
            }
            nuevoBtn.setEnabled(false);
            guardarBtn.setEnabled(true);
        } else {
            new rojerusan.RSNotifyFade("¡¡No hay Selección!! ", "Seleccione un valor en la tabla valido para editar!", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.INFORMATION).setVisible(true);

        }
    }//GEN-LAST:event_editarBtnActionPerformed

    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int fila = tabla.getSelectedRow();
        String id = fila >= 0 ? tabla.getValueAt(fila, 0).toString() : "-";
        if (fila >= 0 && !id.equals("-") && valor < 0) {
            int idint = Integer.valueOf(id);
            for (CondicionMigratoria condicion : condiciones) {
                if (idint == condicion.getId()) {
                    idTxt.setText(condicion.getId() + "");
                    nombreTxt.setText(condicion.getNombre());
                    if (condicion.getEstado().equalsIgnoreCase("activo")) {
                        activoRb.setSelected(true);
                    } else {
                        bloqueadoRb.setSelected(true);
                    }
                }

            }
        } else {
            new rojerusan.RSNotifyFade("¡¡No hay Selección!! ", "Seleccione un valor en la tabla valido para editar!", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.INFORMATION).setVisible(true);

        }
    }//GEN-LAST:event_tablaMouseClicked

    private void guardarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBtnActionPerformed
        if (validarCamposLlenos() == true) {
            String nombre = nombreTxt.getText();

            if (valor == 0) {//nuevo
                boolean repetido = false;
                for (CondicionMigratoria condicion : condiciones) {
                    if (condicion.getNombre().equalsIgnoreCase(nombre)) {
                        repetido = true;
                    }
                }
                if (repetido == false) {
                    int respuesta = new CondicionMigratoria().newCondicion(nombre);
                    if (respuesta > 0) {
                        new rojerusan.RSNotifyFade("¡¡Agregado!! ", "Condición Migratoria agregado con exito!", 4, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
                        limpiarCampos();
                        buscarValorTabla(nombre);
                        ParametrosPanel.actualizarPams("condicion");
                    } else {
                        new rojerusan.RSNotifyFade("¡¡ERROR!! ", "No se pudo agregar los valores, contacte al adm o vuelva intentarlo!", 4, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.ERROR).setVisible(true);

                    }
                } else {
                    new rojerusan.RSNotifyFade("¡¡Valor Repetido!! ", "Los valore deben ser únicos, intente con otro valor y vuelva intentarlo!", 4, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.INFORMATION).setVisible(true);

                }

            } else if (valor == 1) {//editar
                int id = Integer.valueOf(idTxt.getText());
                int respuesta = new CondicionMigratoria().updateGenero(id, nombre, estado);
                if (respuesta > 0) {
                    new rojerusan.RSNotifyFade("¡¡Actualizado!! ", "Condicion Migratoria actualizada con exito!", 4, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
                    limpiarCampos();
                    buscarValorTabla(nombre);
                    ParametrosPanel.actualizarPams("condicion");
                } else {
                    new rojerusan.RSNotifyFade("¡¡ERROR!! ", "Tuvimos problemas para actualizar los valores, contacte al adm o vuelva intentarlo!", 4, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.ERROR).setVisible(true);
                }
            }
        }else{
            new rojerusan.RSNotifyFade("¡¡Campo Vacio!! ", "El campo <Valor> no puede quedar vacio.!", 4, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.INFORMATION).setVisible(true);
        }
    }//GEN-LAST:event_guardarBtnActionPerformed

